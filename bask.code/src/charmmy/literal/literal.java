package charmmy.literal;

public class literal
{
    public static void main(String[] args)
    {
        System.out.println("霸王龙");
//        输出恐龙的年龄：8岁
        System.out.println("8岁");
//        输出恐龙的体重：11.5
        System.out.println(11.5);
//        蔬菜恐龙的性别：公
        System.out.println('公');
    }
}
